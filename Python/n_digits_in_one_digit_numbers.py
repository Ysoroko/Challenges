# Codingame Clash of Code challenge

# 1, 3 -------> 54
# ________
# 1 2 3 4 5 6 7 8 9 --------> 9
# 11 22 33 44 55 66 77 88 99 --------> 18
# 111 222 333 444 555 666 777 888 999 ------> 36
# 9 + 18 + 36 = 54

# 1, 9 ---> 405. (45 * 9)
# 9 + 18 + 36 .....

l = int(input())
r = int(input())

ret = 0

for i in range(l, r + 1):
    ret += 9 * len("1" + "0" * (i - l))

print(ret)
